// Generates assembly/__tests__/float-oracle.spec.ts: a table of
// `JSON.stringify<f64|f32>(x)` assertions whose expected strings are produced by
// an actual JS `JSON.stringify` / Number::toString call. json-as targets
// `JSON.stringify(JSON.parse(payload))`, so every expectation here is the exact
// string JS would emit. Regenerate with: node scripts/gen-float-oracle.mjs
import { writeFileSync } from "fs";

const OUT = "assembly/__tests__/float-oracle.spec.ts";
const RANDOM_F64 = 600;
const RANDOM_F32 = 300;

// Turn a JS Number::toString string into a literal AssemblyScript's lexer
// accepts as a float: drop the "e+" the AS lexer rejects, and append ".0" to a
// bare-integer form so it is lexed as f64 (otherwise large values overflow i64
// before the <f64> coercion). The *expected* string keeps the JS spelling.
const lit = (s) => {
  let t = s.replace("e+", "e");
  if (!/[.eE]/.test(t)) t += ".0";
  return t;
};

// ---- f64: the expected string IS the actual JSON.stringify(x) output --------
const f64Edges = [
  0, 1, -1, 7.23, -7.23, 0.5, -0.5, 0.125, -0.125, 3.14, -3.14, 10e2, 1000, 123,
  42, 100, 1000000, 1e15, 1e16, 1e17, 1e18, 1e19, 1e20, 1e21, 1e22, 123456e-5,
  0.0001, 1e-5, 1e-6, 1e-7, 1e-8, 1e-30, 1e30, 3.141592653589793,
  2.718281828459045, -123456789.25, 9007199254740992, 123456789012345680,
  314000, -981, 6.022e23, 1.7976931348623157e308, 5e-324,
  2.2250738585072014e-308, -0.0000001, 0.0000315, 1234.5678, 0.1, 0.2, 0.3,
];
for (let e = -25; e <= 25; e++) f64Edges.push(Number("1e" + e));

// Deterministic xorshift64 so regeneration is stable.
let state = 0x9e3779b97f4a7c15n;
const MASK = (1n << 64n) - 1n;
const dv = new DataView(new ArrayBuffer(8));
function randDouble() {
  state ^= (state << 13n) & MASK;
  state ^= state >> 7n;
  state ^= (state << 17n) & MASK;
  dv.setBigUint64(0, state);
  return dv.getFloat64(0);
}

const f64Vals = new Set();
for (const x of f64Edges)
  if (Number.isFinite(x) && !Object.is(x, -0)) f64Vals.add(x);
while (f64Vals.size < RANDOM_F64 + f64Edges.length) {
  const x = randDouble();
  if (Number.isFinite(x) && !Object.is(x, -0)) f64Vals.add(x);
}

const f64Lines = [...f64Vals].map((x) => {
  const s = JSON.stringify(x); // the actual JSON call
  return `  expect(JSON.stringify<f64>(${lit(s)})).toBe(${JSON.stringify(s)});`;
});

// ---- f32: JS has no Float32 toString, so derive the shortest round-tripping
// decimal (fewest significant digits that fround back to x), ES-formatted, and
// verify it actually round-trips before emitting. ----------------------------
function f32Shortest(x) {
  if (x === 0) return "0";
  for (let p = 1; p <= 9; p++) {
    const v = parseFloat(x.toPrecision(p));
    if (Math.fround(v) === x) return String(v); // ES Number::toString of the chosen decimal
  }
  return String(x);
}

// Curated f32 values whose shortest decimal is unambiguous (no rounding tie),
// so the exact string can be asserted directly.
const f32Edges = [
  0, 1, -1, 1.5, -1.5, 0.25, -0.25, 3.75, 1.25, 0.5, 100, 1000, 1e20, 0.1, 0.2,
  3.14, -3.5, 1e-7, 1e7, 16777216, 8388608, 0.125, -0.0625, 12.5, 2.5, 1e-20,
];
const f32Lines = [];
for (const x0 of f32Edges) {
  const x = Math.fround(x0);
  const s = f32Shortest(x);
  if (Math.fround(parseFloat(s)) !== x) continue;
  f32Lines.push(
    `  expect(JSON.stringify<f32>(${lit(s)})).toBe(${JSON.stringify(s)});`,
  );
}

// Random f32 values can land on an exact rounding tie (e.g. 3257652.25, equally
// close to "…2" and "…3"); the shortest-decimal rule breaks the tie toward the
// even digit, which a naive JS oracle gets wrong. Assert the robust invariant
// instead: serialize -> parse reproduces the exact f32.
const f32Round = [];
let s32 = 0x1357bd >>> 0;
const seen = new Set();
while (f32Round.length < RANDOM_F32) {
  s32 ^= s32 << 13;
  s32 ^= s32 >>> 17;
  s32 ^= s32 << 5;
  s32 >>>= 0;
  const fa = new Float32Array(1);
  new Uint32Array(fa.buffer)[0] = s32;
  const x = fa[0];
  if (!Number.isFinite(x) || Object.is(x, -0) || seen.has(x)) continue;
  seen.add(x);
  const s = f32Shortest(x);
  if (Math.fround(parseFloat(s)) !== x) continue;
  f32Round.push(
    `  expect(JSON.parse<f32>(JSON.stringify<f32>(${lit(s)}))).toBe(${lit(s)});`,
  );
}

const body = `import { describe, expect } from "as-test";
import { JSON } from "..";

// AUTO-GENERATED by scripts/gen-float-oracle.mjs — do not edit by hand.
// Every expected string below was produced by an actual JS JSON.stringify /
// Number::toString call, so this asserts json-as float output matches
// JSON.stringify(JSON.parse(x)) exactly (across the naive/swar/simd modes).

describe("Żmij f64 output matches JSON.stringify", () => {
${f64Lines.join("\n")}
});

// Non-finite values cannot appear in a JSON payload; json-as renders them per
// Number::toString (JSON.stringify would emit null).
describe("Żmij f64 non-finite + signed zero", () => {
  expect(JSON.stringify<f64>(0.0)).toBe("0");
  expect(JSON.stringify<f64>(-0.0)).toBe("0");
  expect(JSON.stringify<f64>(Infinity)).toBe("Infinity");
  expect(JSON.stringify<f64>(-Infinity)).toBe("-Infinity");
  expect(JSON.stringify<f64>(NaN)).toBe("NaN");
});

describe("Żmij f32 output matches the shortest decimal (exact)", () => {
${f32Lines.join("\n")}
});

describe("Żmij f32 serialize -> parse round-trips exactly", () => {
${f32Round.join("\n")}
});
`;

writeFileSync(OUT, body);
console.log(
  `wrote ${OUT}: ${f64Lines.length} f64 + ${f32Lines.length} f32 exact + ${f32Round.length} f32 round-trip assertions`,
);
