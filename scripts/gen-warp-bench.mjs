// Generates the WARP variant of a classic bench. WARP has no WASI and can't
// re-enter the module from a host import, so it can't read the payload through
// the bench lib's readFile (which allocates via __new). This rewrites the real
// assembly/__benches__/classic/<name>.bench.ts into an equivalent that embeds
// the payload instead - every readFile("...path...") call is replaced by the
// file's contents as a string literal - while keeping the identical schema and
// bench()/dumpToFile calls, so WARP self-measures the same workload via the real
// bench lib (timed by warp_host's performance.now).
//
// Usage: node scripts/gen-warp-bench.mjs <name> <out.ts>
import fs from "node:fs";
import path from "node:path";

const [, , name, outPath] = process.argv;
if (!outPath) {
  console.error("usage: gen-warp-bench.mjs <name> <out.ts>");
  process.exit(1);
}

let src = fs.readFileSync(
  path.resolve(`assembly/__benches__/classic/${name}.bench.ts`),
  "utf8",
);

// 1. Drop the test-only `expect` import and any expect(...) assertion statements
//    (they pull in test infra and aren't part of the measured workload).
src = src
  .split("\n")
  .filter((l) => !/from\s+["'].*__tests__\/lib["']/.test(l))
  .filter((l) => !/^\s*expect\(/.test(l))
  .join("\n");

// 2. Replace every readFile("<path>") (single- or multi-line) with the embedded
//    file contents as a fully-escaped string literal.
src = src.replace(/readFile\(\s*"([^"]+)"\s*,?\s*\)/g, (_m, p) =>
  JSON.stringify(fs.readFileSync(path.resolve(p), "utf8")),
);

// 3. Drop the now-unused readFile import binding so asc doesn't flag it. The
//    import list also brings bench/dumpToFile/blackbox/utf8ByteLength, which stay.
src = src.replace(/^\s*readFile,\s*$/m, "");

// The full iteration counts are kept: WARP destabilizes only under one long
// single-shot allocation loop, which run-bench.runtimes.sh avoids by building
// with BENCH_FRAMES (the bench lib splits the run into small GC-separated
// frames). No per-bench cap is needed.

const header = `// AUTO-GENERATED by scripts/gen-warp-bench.mjs - do not edit.
// WARP build of the "${name}" classic bench: identical schema + bench() calls,
// but payloads are embedded (WARP has no WASI / host re-entrancy). Measured by
// the real bench lib; results emitted via warp_host's writeFile shim.
`;

fs.writeFileSync(path.resolve(outPath), header + src);
console.log(`> ${outPath}`);
