// Generates a self-contained deserialize bench for the warpo compiler
// (wasm-ecosystem/warpo, a "next-gen AssemblyScript compiler"). warpo can't run
// asc transform plugins, and its stdlib lacks asc's ambient `console`/
// `performance` globals and the transform-generated BenchResult serializer that
// the shared bench() lib depends on. So this emits a harness that:
//   * lifts the @json schema from classic/<name>.bench.ts (it gets the
//     transform-generated __DESERIALIZE when this file is run through asc),
//   * embeds the minified payload,
//   * declares its OWN env imports (now/log) instead of console/performance,
//   * times a warmed-up, framed deserialize loop and prints one JSON result line.
//
// The same generated source is compiled two ways for an apples-to-apples
// COMPILER comparison on one runtime (v8):
//   asc:   npx asc <src> --transform ./transform ...      (baseline)
//   warpo: asc emits <src>.tmp.ts (post-transform), warpo build <src>.tmp.ts ...
//
// Usage: node scripts/gen-warpo-bench.mjs <name> <RootType> <checksumExpr> <out.ts>
import fs from "node:fs";
import path from "node:path";

const [, , name, rootType, checksumExpr, outPath] = process.argv;
if (!outPath) {
  console.error(
    "usage: gen-warpo-bench.mjs <name> <RootType> <checksumExpr> <out.ts>",
  );
  process.exit(1);
}

const benchSrc = fs.readFileSync(
  path.resolve(`assembly/__benches__/classic/${name}.bench.ts`),
  "utf8",
);
const lines = benchSrc.split("\n");
const startIdx = lines.findIndex((l) => l.trimStart().startsWith("@json"));
const endIdx = lines.findIndex((l) => /readFile\(/.test(l));
if (startIdx < 0 || endIdx < 0)
  throw new Error(`schema block not found in ${name}.bench.ts`);
const schema = lines.slice(startIdx, endIdx).join("\n").trimEnd();

const payload = fs.readFileSync(
  path.resolve(`assembly/__benches__/payloads/${name}.min.json`),
  "utf8",
);
const embedded = JSON.stringify(payload);

const out = `// AUTO-GENERATED by scripts/gen-warpo-bench.mjs - do not edit.
// Self-contained warpo/asc deserialize bench for the "${name}" classic payload.
import { JSON } from "../..";

// env imports instead of asc's ambient console/performance (which warpo lacks).
// @ts-ignore: decorator
@external("env", "now")
declare function now(): f64;
// @ts-ignore: decorator
@external("env", "log")
declare function log(msg: string): void;

${schema}

const PAYLOAD: string = ${embedded};
const OPS: i32 = 2000;
const FRAMES: i32 = 40;

// Timer latency (ms per now() call), set by calibration in run(). Each frame
// brackets its work with two now() calls, so ~one now()-latency leaks into every
// frame's interval; emit() subtracts framesRun * nowLatency.
let nowLatency: f64 = 0;
const BYTES: f64 = <f64>String.UTF8.byteLength(PAYLOAD);

function emit(kind: string, elapsed: f64, framesRun: i32, checksum: f64): void {
  const corrected: f64 = elapsed - <f64>framesRun * nowLatency;
  const mbps: f64 = (BYTES * <f64>OPS) / (corrected / 1000.0) / 1.0e6;
  log(
    '{"description":"' + kind + ' ${name} (min)","kind":"' + kind +
      '","bytes":' + BYTES.toString() +
      ',"operations":' + OPS.toString() +
      ',"mbps":' + mbps.toString() +
      ',"nowLatencyNs":' + (nowLatency * 1.0e6).toString() +
      ',"elapsedMs":' + elapsed.toString() +
      ',"correctedMs":' + corrected.toString() +
      ',"checksum":' + checksum.toString() + "}",
  );
}

// Warmed-up, framed measurement (a full __collect between frames keeps each
// frame's allocation churn bounded; between-frame GC is untimed). Mirrors the
// bench() lib's methodology without its console/performance/BenchResult deps.
export function run(): void {
  const perFrame: i32 = OPS / FRAMES;

  // Calibrate the timer: average cost of a now() call (ms).
  const CAL: i32 = 200000;
  let sink: f64 = 0;
  const calStart: f64 = now();
  for (let i = 0; i < CAL; i++) sink += now();
  nowLatency = (now() - calStart) / <f64>CAL;
  if (sink < 0) log(""); // keep the calibration loop from being optimized away

  // --- deserialize ---
${
  process.env.REUSE
    ? `  // reuse-target parse: deserialize into an existing object to slash allocation
  // (and thus GC pressure), isolating codegen from GC cost.
  const target = JSON.parse<${rootType}>(PAYLOAD);`
    : ""
}
  let warm: i32 = perFrame;
  while (warm-- > 0) JSON.parse<${rootType}>(PAYLOAD${process.env.REUSE ? ", target" : ""});
  {
    let elapsed: f64 = 0;
    let remaining: i32 = OPS;
    let checksum: f64 = 0;
    let framesRun: i32 = 0;
    while (remaining > 0) {
      const batch: i32 = remaining < perFrame ? remaining : perFrame;
      __collect();
      const start: f64 = now();
      for (let i = 0; i < batch; i++) {
        const parsed = JSON.parse<${rootType}>(PAYLOAD${process.env.REUSE ? ", target" : ""});
        checksum += <f64>(${checksumExpr});
      }
      elapsed += now() - start;
      framesRun++;
      remaining -= batch;
    }
    emit("deserialize", elapsed, framesRun, checksum);
  }

  // --- serialize ---
  const obj = JSON.parse<${rootType}>(PAYLOAD);
  warm = perFrame;
  while (warm-- > 0) JSON.stringify(obj);
  {
    let elapsed: f64 = 0;
    let remaining: i32 = OPS;
    let checksum: f64 = 0;
    let framesRun: i32 = 0;
    while (remaining > 0) {
      const batch: i32 = remaining < perFrame ? remaining : perFrame;
      __collect();
      const start: f64 = now();
      for (let i = 0; i < batch; i++) {
        const s = JSON.stringify(obj);
        checksum += <f64>s.length;
      }
      elapsed += now() - start;
      framesRun++;
      remaining -= batch;
    }
    emit("serialize", elapsed, framesRun, checksum);
  }
}
`;

fs.writeFileSync(path.resolve(outPath), out);
console.log(`> ${outPath} (payload ${(payload.length / 1024).toFixed(0)}KB)`);
